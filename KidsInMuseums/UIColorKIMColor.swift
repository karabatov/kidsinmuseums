extension UIColor {
    class func kimColor() -> UIColor {
        return UIColor(red: 127.0/255.0, green: 86.0/255.0, blue: 149.0/255.0, alpha: 1.0)
    }

    class func kimOrangeColor() -> UIColor {
        return UIColor(red: 231.0/255.0, green: 121.0/255.0, blue: 43.0/255.0, alpha: 1.0)
    }

    class func kimGrayColor() -> UIColor {
        return UIColor(red: 151.0/255.0, green: 151.0/255.0, blue: 151.0/255.0, alpha: 1.0)
    }

    class func kimDarkGrayColor() -> UIColor {
        return UIColor(red: 74.0/255.0, green: 74.0/255.0, blue: 74.0/255.0, alpha: 1.0)
    }
}